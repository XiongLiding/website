	<h3>版权声明</h3>
	<div>
	PostgreSQL 使用PostgreSQL版权声明，它是类似BSD的版权。由于这个经 OSI认证的版权不限制PostgreSQL在商业环境和有
	版权的应用程序中使用，因此被公认为是非常有灵活性和对商业应用是友好的。加上有多个公司的支持和源代码归属公共所有，
	我们的版权使得PostgreSQL在那些希望在自己的产品里嵌入数据库的厂商中很流行，因为他们不用担心费用、嵌入软件的版权
	锁以及版权条款的改变。 
	</div>



