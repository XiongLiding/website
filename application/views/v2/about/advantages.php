<p class=ch_indent>PostgreSQL为您的公司或是商业应用提供很多有别于其他数据库很多优点：</p>

<h4>对超许可数量软件使用的天然免疫力</h4>

<p class=ch_indent>对一些商业性质的数据库销售商来说，超许可数量的软件使用是第一位的问题。而使用PostgreSQL，因为没有任何授权的费用是，也就没有任何人可以起诉您违反授权协议违规使用软件。<br>

<UL>这样也还有以下几个其他的优点：
	<LI>随着使用数量的增加不会影响业务的利润。
	<LI>在任何阶段都不会有类似授权审计的打扰。
    <LI>在做理论性研究和测试性的发布时不必考虑额外授权费用的影响。
</UL>

<h4>比商业服务商更好的支持</h4>

<p class=ch_indent>除了有一般商业公司的支持外，我们还有由PostgreSQL专业人员和热心的爱好者组成的各种社区，用户可以向他们寻求支持和帮助。</p>

<h4>员工成本的显著降低</h4>

<p class=ch_indent>相对于市场上大型的商业私有数据库软件，我们的系统在设计和开发时已考虑仅需少量的维护和优化，同时仍保持所有功能正常、稳定和性能正常。</p>

<p class=ch_indent>不仅如此，我们的培训过程相对于那些商业私有数据库供应商来说，总体来说有更好的性价比、更容易管理和更接近真实的使用场景。</p>

<h4>享有盛名的可靠性和稳定性</h4>

<p class=ch_indent>与很多商业私有数据库不一样的是，对很多公司特别常见的是，PostgreSQL在几年的运行过程中几乎未出现哪怕是一次的宕机情况，即使是一次，它就是这样稳定地工作着。</p>

<h4>扩展性</h4>

<p class=ch_indent>所有PostgreSQL的源代码对所有人都是可以免费获得的。如果您的公司员工需要定制或是给PostgreSQL增加一些功能，他们只需做很少的改动工作，也没有额外的成本。全球PostgreSQL社区的专业人员和热心的爱好者也在积极地扩展PostgreSQL的功能。</p>

<h4>跨平台能力</h4>

<p class=ch_indent>PostgreSQL几乎可以所有Unix类操作系统下运行 (根据统计有至少34种平台)，自8.0版本以后，在Windows平台下的也使用原生支持方式。</p>

<h4>大数据量环境下的设计</h4>

<p class=ch_indent>我们使用MVCC模式作为数据存储的策略，这样PostgreSQL可以在大数据量的环境下具有极高的响应性能。一些领先的商业私有数据库供应商基于相同的原因，也使用这种技术。</p>

<h4>图形化的数据库设计和管理工具</h4>

<p class=ch_indent>有很多高品质的图形化的PostgreSQL管理工具软件，包括开源和商业性质的。在我们的维基网站上有一个清单列出了PostgreSQL相关的图形化工具软件。</p>

